def _prepare(module, fixture: dict[str, Path], role: str) -> int:
    arguments = [
        "--role",
        role,
        "--host-city",
        str(fixture["host_city"]),
        "--worker-city",
        str(fixture["worker_city"]),
        "--output-dir",
        str(fixture["output"]),
    ]
    if role in {"hq", "mayor"}:
        arguments.extend(("--hq-beads-config", str(fixture["hq_config"])))
    if role in {"aegis", "mayor"}:
        arguments.extend(
            (
                "--aegis-beads-config",
                str(fixture["aegis_config"]),
                "--aegis-metadata",
                str(fixture["metadata"]),
            )
        )
    return module.main(arguments)


def test_prepare_mayor_boundary_is_exact_private_and_idempotent(tmp_path: Path) -> None:
    module = _load_extensionless("prepare_worker_boundary_test", PREPARE)
    fixture = _boundary_fixture(tmp_path)

    assert _prepare(module, fixture, "mayor") == 0
    assert _prepare(module, fixture, "mayor") == 0

    output = fixture["output"]
    assert {path.name for path in output.iterdir()} == {
        "aegis-config.yaml",
        "aegis-metadata.json",
        "host-city.toml",
        "hq-config.yaml",
        "worker-boundary.json",
    }
    assert all(
        stat.S_IMODE(path.stat().st_mode) == 0o600 and path.stat().st_nlink == 1
        for path in output.iterdir()
    )
    assert (output / "host-city.toml").read_bytes() == fixture["host_city"].read_bytes()
    assert (output / "hq-config.yaml").read_bytes() == module.HQ_CONFIG_OVERLAY
    assert (output / "aegis-config.yaml").read_bytes() == module.AEGIS_CONFIG_OVERLAY
    projected = json.loads((output / "aegis-metadata.json").read_text())
    assert projected["dolt_server_host"] == "gas-city-aegis-dolt"
    assert projected["dolt_server_port"] == 3306
    assert projected["project_id"] == "74eb5b48-1390-4fc8-a88d-2b979bcfa7b5"
    receipt = json.loads((output / "worker-boundary.json").read_text())
    assert receipt["role"] == "mayor"
    assert receipt["host_city_sha256"] == hashlib.sha256(
        fixture["host_city"].read_bytes()
    ).hexdigest()
    assert receipt["aegis_metadata_projected_sha256"] == hashlib.sha256(
        (output / "aegis-metadata.json").read_bytes()
    ).hexdigest()


