def _secret(path: Path, value: str) -> Path:
    _write(path, value + "\n", 0o600)
    return path


@pytest.mark.parametrize(
    ("role", "sections"),
    [
        ("hq", ["gas-city-hq-dolt:3306"]),
        ("aegis", ["gas-city-aegis-dolt:3306"]),
        ("mayor", ["gas-city-hq-dolt:3306", "gas-city-aegis-dolt:3306"]),
    ],
)
def test_supervisor_materializes_only_role_scoped_credentials_in_tmpfs(
    tmp_path: Path,
    monkeypatch: pytest.MonkeyPatch,
    role: str,
    sections: list[str],
) -> None:
    module = _load_supervisor()
    home = _private(tmp_path / "worker-home")
    primary = _secret(tmp_path / "primary", "H" * 40)
    aegis = _secret(tmp_path / "aegis", "A" * 40)
    credentials = home / ".config/beads/credentials"
    monkeypatch.setattr(module, "WORKER_HOME_PATH", home)
    monkeypatch.setattr(module, "PASSWORD_SECRET_PATH", primary)
    monkeypatch.setattr(module, "AEGIS_PASSWORD_SECRET_PATH", aegis)
    monkeypatch.setattr(module, "DOLT_CREDENTIALS_PATH", credentials)
    monkeypatch.setattr(module, "_exact_mount_type", lambda path: "tmpfs" if path == home else None)
    scoped_user = "aegis_beads" if role == "aegis" else "gas_city_hq"
    scoped_database = "aegis_beads" if role == "aegis" else "hq"
    monkeypatch.setenv("GC_DOLT_USER", scoped_user)
    monkeypatch.setenv("GC_DOLT_DATABASE", scoped_database)
    monkeypatch.setenv("GC_DOLT_PASSWORD", "must-disappear")
    monkeypatch.setenv("BEADS_DOLT_PASSWORD", "must-disappear")
    monkeypatch.setenv("BEADS_DOLT_SERVER_DATABASE", scoped_database)

    assert module.prepare_dolt_credentials(role) == credentials
    content = credentials.read_text(encoding="utf-8")
    assert [line[1:-1] for line in content.splitlines() if line.startswith("[")] == sections
    assert stat.S_IMODE(credentials.stat().st_mode) == 0o600
    assert os.environ["BEADS_CREDENTIALS_FILE"] == str(credentials)
    if role == "mayor":
        assert "GC_DOLT_USER" not in os.environ
        assert "GC_DOLT_DATABASE" not in os.environ
        assert "BEADS_DOLT_SERVER_DATABASE" not in os.environ
    else:
        assert os.environ["GC_DOLT_USER"] == scoped_user
        assert os.environ["GC_DOLT_DATABASE"] == scoped_database
        assert os.environ["BEADS_DOLT_SERVER_DATABASE"] == scoped_database
    assert "GC_DOLT_PASSWORD" not in os.environ
    assert "BEADS_DOLT_PASSWORD" not in os.environ


