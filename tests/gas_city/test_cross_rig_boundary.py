def test_supervisor_validates_mayor_worker_and_nested_scope_overlays(
    tmp_path: Path, monkeypatch: pytest.MonkeyPatch
) -> None:
    prepare = _load_extensionless("prepare_worker_boundary_supervisor_test", PREPARE)
    module = _load_supervisor()
    fixture = _boundary_fixture(tmp_path)
    assert _prepare(prepare, fixture, "mayor") == 0

    city = fixture["city"]
    rig = fixture["rig"]
    shutil.copyfile(fixture["worker_city"], city / "city.toml")
    (city / "city.toml").chmod(0o600)
    shutil.copyfile(fixture["output"] / "hq-config.yaml", city / ".beads/config.yaml")
    (city / ".beads/config.yaml").chmod(0o600)
    shutil.copyfile(fixture["output"] / "aegis-config.yaml", rig / ".beads/config.yaml")
    shutil.copyfile(fixture["output"] / "aegis-metadata.json", rig / ".beads/metadata.json")
    (rig / ".beads/config.yaml").chmod(0o600)
    (rig / ".beads/metadata.json").chmod(0o600)
    workdir = _private(city / ".gc/agents/mayor/work")
    image_worker = _write(tmp_path / "image/city.worker.toml", fixture["worker_city"].read_bytes(), 0o444)

    monkeypatch.setattr(module, "IMAGE_ROOT_UID", os.getuid())
    monkeypatch.setattr(module, "IMAGE_WORKER_CITY_CONFIG_PATH", image_worker)
    monkeypatch.setattr(module, "HOST_CITY_CONFIG_PATH", fixture["output"] / "host-city.toml")
    monkeypatch.setattr(module, "HQ_CONFIG_OVERLAY_PATH", fixture["output"] / "hq-config.yaml")
    monkeypatch.setattr(module, "AEGIS_CONFIG_OVERLAY_PATH", fixture["output"] / "aegis-config.yaml")
    monkeypatch.setattr(module, "AEGIS_METADATA_OVERLAY_PATH", fixture["output"] / "aegis-metadata.json")
    monkeypatch.setattr(module, "WORKER_BOUNDARY_RECEIPT_PATH", fixture["output"] / "worker-boundary.json")
    identity = dict(module.AEGIS_RUNTIME_IDENTITY)
    identity["GC_RIG_ROOT"] = str(rig)
    monkeypatch.setattr(module, "AEGIS_RUNTIME_IDENTITY", identity)
    environment = {
        "GC_CITY_ROOT": str(city),
        "GC_WORKER_BOUNDARY_ROLE": "mayor",
        "GC_AGENT": "gastown.mayor",
        "GC_ALIAS": "gastown.mayor",
        "GC_TEMPLATE": "gastown.mayor",
        "GC_SESSION_ORIGIN": "named",
        "GC_HOST_CITY_CONFIG": str(module.HOST_CITY_CONFIG_PATH),
        "GC_IMAGE_WORKER_CITY_CONFIG": str(module.IMAGE_WORKER_CITY_CONFIG_PATH),
        "GC_HQ_CONFIG_OVERLAY": str(module.HQ_CONFIG_OVERLAY_PATH),
        "GC_AEGIS_CONFIG_OVERLAY": str(module.AEGIS_CONFIG_OVERLAY_PATH),
        "GC_AEGIS_METADATA_OVERLAY": str(module.AEGIS_METADATA_OVERLAY_PATH),
    }
    for key, value in environment.items():
        monkeypatch.setenv(key, value)
    monkeypatch.delenv("GC_RIG", raising=False)
    monkeypatch.chdir(workdir)

    assert module.validate_worker_boundary() == "mayor"
    monkeypatch.setenv("GC_WORKER_BOUNDARY_ROLE", "hq")
    with pytest.raises(RuntimeError, match="did not receive the Mayor boundary"):
        module.validate_worker_boundary()


